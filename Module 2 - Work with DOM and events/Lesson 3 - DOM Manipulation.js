/*
1. Выбор элементов DOM:

- `getElementById`: выбор элемента по его уникальному идентификатору (id).
- `getElementsByClassName`: выбор всех элементов с определенным классом.
- `getElementsByTagName`: выбор всех элементов с определенным тегом.
- `querySelector`: выбор первого элемента, соответствующего указанному селектору CSS.
- `querySelectorAll`: выбор всех элементов, соответствующих указанному селектору CSS.



2. Изменение содержимого элементов:

- `textContent`: установка или получение текстового содержимого элемента.
- `innerHTML`: установка или получение HTML-содержимого элемента.



3. Изменение атрибутов элементов:

- `getAttribute`: получение значения атрибута элемента.
- `setAttribute`: установка значения атрибута элемента.
- `removeAttribute`: удаление атрибута элемента.



4. Изменение стилей элементов:

- `style.property`: установка или получение значения определенного свойства стиля элемента.
- `classList.add`: добавление класса к элементу.
- `classList.remove`: удаление класса из элемента.
- `classList.toggle`: переключение класса элемента.
- `style.position`: установка позиции элемента.
- `style.left` и `style.top`: установка координаты элемента.
- `style.width` и `style.height`: установка ширины и высоты элемента.
- `style.setProperty`: установка значения свойства стиля элемента.
- `style.removeProperty`: удаление свойства стиля элемента.
- `classList.contains`: проверка, содержит ли элемент определенный класс.



5. Добавление и удаление элементов:

- `createElement`: создание нового элемента.
- `appendChild`: добавление элемента внутрь другого элемента.
- `removeChild`: удаление элемента из его родительского элемента.
- `document.createTextNode`: создание текстового узла.
- `element.insertBefore`: вставка элемента перед другим элементом.



6. Работа с родительскими и дочерними элементами:

- `parentNode`: доступ к родительскому элементу.
- `childNodes`: доступ к списку дочерних элементов.
- `firstChild`: доступ к первому дочернему элементу.
- `lastChild`: доступ к последнему дочернему элементу.
- `nextSibling`: доступ к следующему соседнему элементу.
- `previousSibling`: доступ к предыдущему соседнему элементу.



7. Обработка событий:

- `addEventListener`: добавление обработчика события.
- `removeEventListener`: удаление обработчика события.
- `onclick`: добавление обработчика события.



9. Навигация по DOM:

- `querySelector` и `querySelectorAll` (селекторы CSS): выбор элементов на основе определенных критериев.



10. Получение координат и размеров элемента:

- `offsetTop` и `offsetLeft`: получение вертикального и горизонтального смещения элемента
    относительно его родительского элемента.
- `offsetWidth` и `offsetHeight`: получение ширины и высоты элемента, включая границы и
    заполнение, но не включая отступы.



14. Перебор элементов:

- `forEach`: перебор массива элементов DOM и выполнение операций с каждым элементом.



16. Прокрутка элементов:

- `scrollTop` и `scrollLeft`: получение или установка вертикальной и горизонтальной прокрутки элемента.
- `scrollIntoView`: прокрутка элемента в область видимости окна.
*/
